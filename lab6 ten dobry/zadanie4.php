  <?php
    function multiply_matrices($A, $B) {
        $rowsA = count($A);
        $colsA = count($A[0]);
        $rowsB = count($B);
        $colsB = count($B[0]);

        //lkolumnA = lwierszyB
        if ($colsA !== $rowsB) {
            echo "Błąd: Wymiary macierzy są niezgodne do mnożenia.<br>";
            return;
        }


        $result = [];
        for ($i = 0; $i < $rowsA; $i++) {
            for ($j = 0; $j < $colsB; $j++) {
                $sum = 0;
                for ($k = 0; $k < $colsA; $k++) {
                    $sum += $A[$i][$k] * $B[$k][$j];
                }
                $result[$i][$j] = $sum;
            }
        }


        echo "<p>Wynik mnożenia macierzy:</p><pre>";
        foreach ($result as $row) {
            echo implode("\t", $row) . "\n";
        }
        echo "</pre><br>";
    }

    // Przykładowe macierze
    $A = [
        [1, 2, 3],
        [4, 5, 6]
    ];

    $B = [
        [7, 8],
        [9, 10],
        [11, 12]
    ];

    multiply_matrices($A, $B);
  ?>
